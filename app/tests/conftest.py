def has_kannada_chars(text: str) -> bool:
    """True if ``text`` contains at least one character in the Kannada block.

    Kannada Unicode block: U+0C80–U+0CFF.
    """
    return any("ಀ" <= ch <= "೿" for ch in text or "")


def has_latin_chars(text: str) -> bool:
    """True if ``text`` contains at least one ASCII letter."""
    return any(ch.isascii() and ch.isalpha() for ch in text or "")


