    """Factory: ``test_user_token("PSI")`` returns a signed-shape token."""

    def _make(role: str, user_id: str | None = None) -> str:
        spec = ROLE_SCOPES.get(role.upper())
        if spec is None:
            raise ValueError(
                f"Unknown demo role: {role!r} (choose PSI, SHO, or DCP)",
            )
        claims = {
            "sub": user_id or f"user-{role.lower()}-001",
            "role": spec["role"],
            "station": spec["station"],
            "district": spec["district"],
            "scope": spec["scope"],
            "masked_fields": spec["masked_fields"],
            "iat": 1_750_000_000,
        }
        return _encode_fake_token(claims)

    return _make


