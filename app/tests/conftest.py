class FakeGeminiClient:
    """In-memory stand-in for ``shared.gemini_client``.

    Handles three call shapes used in the codebase:

    * ``generate(prompt, language)`` — Kannada/English synthesis
    * ``embed(text)``                — multilingual embedding
    * ``live_stream(audio_bytes)``   — Gemini Live STT+TTS round trip
    """

    def __init__(self) -> None:
        self.responses: dict[str, Any] = {}
        self.calls: list[tuple[str, Any]] = []
        # Sensible defaults so synthesizer tests don't have to wire each time.
        self.responses.setdefault("generate", "Synthesized answer.")
        self.responses.setdefault("generate_kn", "ಸಂಶ್ಲೇಷಿತ ಉತ್ತರ.")
        self.responses.setdefault("embed", [0.0] * 768)
        self.responses.setdefault(
            "live_stream",
            {"transcript": "test transcript", "audio_b64": "AAAA",
             "language": "en"},
        )

    def generate(self, prompt: str, language: str = "en") -> str:
        self.calls.append(("generate", {"prompt": prompt, "language": language}))
        if language == "kn":
            return self.responses.get("generate_kn", self.responses["generate"])
        return self.responses["generate"]

    def embed(self, text: str) -> list[float]:
        self.calls.append(("embed", text))
        return self.responses["embed"]

    def live_stream(self, audio_bytes: bytes, language_hint: str = "auto") -> dict:
        self.calls.append(("live_stream", {"bytes": len(audio_bytes),
                                           "lang_hint": language_hint}))
        return self.responses["live_stream"]


