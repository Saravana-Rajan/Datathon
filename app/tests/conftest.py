class FakeCatalystClient:
    """In-memory stand-in for ``shared.catalyst_client``.

    Mimics the methods that the real client exposes:

    * ``quickml_llm(prompt, model)``      — canned LLM completions
    * ``data_store_query(sql)``           — canned SQL row sets
    * ``invoke_function(name, payload)``  — canned Catalyst Function calls
    * ``append_audit(step)``              — captures audit chain in-memory

    Tests can prime expected outputs via the ``responses`` dict and
    afterwards inspect ``calls`` to verify what was asked of it.
    """

    def __init__(self) -> None:
        self.responses: dict[str, Any] = {}
        self.calls: list[tuple[str, Any]] = []
        self.audit_chain: list[dict[str, Any]] = []
        self.fail_on: set[str] = set()  # names that should raise

    # ---- LLM ----
    def quickml_llm(self, prompt: str, model: str = "qwen2.5-7b") -> str:
        self.calls.append(("quickml_llm", {"prompt": prompt, "model": model}))
        if "quickml_llm" in self.fail_on:
            raise RuntimeError("quickml_llm forced failure")
        return self.responses.get(
            "quickml_llm",
            '{"intent":"lookup","language":"en","confidence":0.5,"entities":{}}',
        )

    # ---- Data Store ----
    def data_store_query(self, sql: str, params: dict | None = None) -> list[dict]:
        self.calls.append(("data_store_query", {"sql": sql, "params": params}))
        if "data_store_query" in self.fail_on:
            raise RuntimeError("data_store_query forced failure")
        return self.responses.get("data_store_query", [])

    # ---- Function fan-out ----
    def invoke_function(self, name: str, payload: dict) -> dict:
        self.calls.append((f"invoke:{name}", payload))
        if name in self.fail_on:
            raise RuntimeError(f"{name} forced failure")
        return self.responses.get(f"invoke:{name}", {})

    # ---- Audit ----
    def append_audit(self, step: dict) -> dict:
        self.calls.append(("append_audit", step))
        idx = len(self.audit_chain)
        record = {"step_index": idx, **step}
        self.audit_chain.append(record)
        return {"ok": True, "step_index": idx}


