def test_h3_indexing_works_on_every_record(
    sample_firs: list[dict[str, Any]],
) -> None:
    """Every (location_lat, location_lng) must yield a valid H3 res-8 cell.

    If the ``h3`` package isn't installed we skip — the hotspot pipeline
    is the only place that needs it and it's optional for unit runs.
    """
    h3 = pytest.importorskip("h3", reason="h3 package not installed")

    cells: set[str] = set()
    for rec in sample_firs:
        # h3-py 4.x exposes ``latlng_to_cell``; older 3.x uses ``geo_to_h3``.
        latlng_to_cell = getattr(h3, "latlng_to_cell", None) or getattr(
            h3, "geo_to_h3", None,
        )
        assert latlng_to_cell is not None, "h3 API surface unknown"
        cell = latlng_to_cell(rec["location_lat"], rec["location_lng"], 8)
        assert isinstance(cell, str) and len(cell) >= 10, (
            f"{rec['fir_no']} bad H3 cell: {cell!r}"
        )
        cells.add(cell)

    # With 100 FIRs scattered across Karnataka we expect lots of unique cells.
    assert len(cells) >= 20, (
        f"only {len(cells)} unique H3 cells — geo distribution too clumpy"
    )
