def test_crime_type_distribution_is_realistic(
    sample_firs: list[dict[str, Any]],
) -> None:
    """Petty crime should outweigh serious crime by at least 5x.

    NCRB Karnataka 2022 ratio for theft-family : murder is roughly 100:1;
    we'll just demand >5:1 to keep the test robust to sampling noise on
    a 100-record window.
    """
    crime_counts = Counter(rec["crime_type"] for rec in sample_firs)
    # Sum the petty/property crime families that should dominate.
    property_crimes = sum(
        crime_counts.get(c, 0) for c in (
            "theft", "vehicle_theft", "burglary", "chain_snatching",
            "pickpocketing", "house_break_in",
        )
    )
    serious_crimes = sum(
        crime_counts.get(c, 0) for c in (
            "murder", "rape", "kidnapping", "rioting", "dacoity",
        )
    )
    if serious_crimes == 0:
        # Pure petty-crime corpus also acceptable for the sample window.
        assert property_crimes > 0, f"no crimes at all? {crime_counts}"
        return
    ratio = property_crimes / max(serious_crimes, 1)
    assert ratio >= 5, (
        f"crime mix unrealistic: property={property_crimes} "
        f"serious={serious_crimes} ratio={ratio:.2f}; counts={dict(crime_counts)}"
    )


def test_at_least_three_districts_represented(
    sample_firs: list[dict[str, Any]],
) -> None:
    """Karnataka has 31 districts; a 100-record sample should hit several."""
    districts = {rec["district"] for rec in sample_firs}
    assert len(districts) >= 3, (
        f"only {len(districts)} districts represented: {districts}"
    )


def test_at_least_two_years_represented(
    sample_firs: list[dict[str, Any]],
) -> None:
    """Trend queries need multi-year data."""
    years = {rec["date_registered"][:4] for rec in sample_firs}
    assert len(years) >= 2, f"only one year present: {years}"


def test_complainant_phone_is_masked(
    sample_firs: list[dict[str, Any]],
) -> None:
    """Generator should already mask phone numbers (privacy by default)."""
    leaks = [
        rec["fir_no"]
        for rec in sample_firs
        if rec["complainant"].get("phone")
        and not re.search(r"X{4,}", rec["complainant"]["phone"])
    ]
    assert not leaks, (
        f"{len(leaks)} complainant phones not masked; first 5: {leaks[:5]}"
    )


